package com.broll.pokllib.poklmon;

public class PoklmonAttributes {

	
	private int baseKP;
	private int baseAttack;
	private int baseDefence;
	private int baseInitiative;
	private int baseSpecialAttack;
	private int baseSpecialDefence;
	

	
	public int getBaseKP() {
		return baseKP;
	}
	public void setBaseKP(int baseKP) {
		this.baseKP = baseKP;
	}
	public int getBaseAttack() {
		return baseAttack;
	}
	public void setBaseAttack(int baseAttack) {
		this.baseAttack = baseAttack;
	}
	public int getBaseDefence() {
		return baseDefence;
	}
	public void setBaseDefence(int baseDefence) {
		this.baseDefence = baseDefence;
	}
	public int getBaseInitiative() {
		return baseInitiative;
	}
	public void setBaseInitiative(int baseInitiative) {
		this.baseInitiative = baseInitiative;
	}
	public int getBaseSpecialAttack() {
		return baseSpecialAttack;
	}
	public void setBaseSpecialAttack(int baseSpecialAttack) {
		this.baseSpecialAttack = baseSpecialAttack;
	}
	public int getBaseSpecialDefence() {
		return baseSpecialDefence;
	}
	public void setBaseSpecialDefence(int baseSpecialDefence) {
		this.baseSpecialDefence = baseSpecialDefence;
	}
	
	
	
}
